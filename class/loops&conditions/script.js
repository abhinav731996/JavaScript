let score = 78;

switch (true) {
    case score >= 80:
        console.log("A");
        break;

    case score >= 60:
        console.log("B");
        break;

    case score >= 40:
        console.log("C");
        break;

    case score >= 30:
        console.log("D");
        break;

    default:
        console.log("Fail");
        break;
}